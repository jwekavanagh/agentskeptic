#!/usr/bin/env node
/**
 * Writes docs/partner-quickstart-commands.md (command SSOT for partner quickstart).
 * Usage: node scripts/generate-partner-quickstart-commands.mjs [--write|--check]
 * Default: --write. --check exits 1 if file would change.
 */
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outPath = path.join(root, "docs", "partner-quickstart-commands.md");

const body = `<!-- Generated by scripts/generate-partner-quickstart-commands.mjs — do not edit by hand. -->

# Partner quickstart — commands

All copy-paste shell commands for the first partner run live in this file. Narrative and guarantees: [first-run-integration.md](first-run-integration.md).

## Prerequisites

- **Node.js** ≥ 22.13 (repo root)
- **Once:** \`npm install\` at repository root
- **Once:** \`npm run build\` at repository root (produces \`dist/cli.js\`)

## Fast path (SQLite, automated)

From the **repository root**:

\`\`\`bash
npm run partner-quickstart
\`\`\`

This seeds a temporary SQLite database, runs verification, and exits non-zero on failure.

## Postgres (single env var)

Set **only** \`PARTNER_POSTGRES_URL\` to a connection string with permission to run DDL for the seed (\`CREATE TABLE\` / \`INSERT\`). From **repository root**:

\`\`\`bash
export PARTNER_POSTGRES_URL="postgresql://USER:PASS@HOST:5432/DBNAME"
npm run partner-quickstart
\`\`\`

## Manual CLI (SQLite, temp DB you manage)

After seeding with \`examples/partner-quickstart/partner.seed.sql\`:

\`\`\`bash
node dist/cli.js --workflow-id wf_partner --events examples/partner-quickstart/partner.events.ndjson --registry examples/partner-quickstart/partner.tools.json --db path/to/your.db
\`\`\`

## Manual CLI (Postgres)

\`\`\`bash
node dist/cli.js --workflow-id wf_partner --events examples/partner-quickstart/partner.events.ndjson --registry examples/partner-quickstart/partner.tools.json --postgres-url "$PARTNER_POSTGRES_URL"
\`\`\`
`;

const mode = process.argv.includes("--check") ? "check" : "write";

if (mode === "check") {
  let existing = "";
  try {
    existing = readFileSync(outPath, "utf8");
  } catch {
    console.error("check: missing", outPath);
    process.exit(1);
  }
  if (existing !== body) {
    console.error("check: docs/partner-quickstart-commands.md is out of date; run: node scripts/generate-partner-quickstart-commands.mjs");
    process.exit(1);
  }
  process.exit(0);
}

writeFileSync(outPath, body, "utf8");
console.log("Wrote", outPath);
