/**
 * Writes website/src/generated/epistemicContractIntegrator.ts from docs/epistemic-contract.md snippet fence.
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { readIntegratorSnippet } from "./lib/readEpistemicContractFence.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outDir = join(root, "website", "src", "generated");
const snippet = readIntegratorSnippet(root);

mkdirSync(outDir, { recursive: true });

const header = `// AUTO-GENERATED by scripts/sync-epistemic-contract-website.mjs — do not edit.

`;

const escaped = JSON.stringify(snippet);
const body = `${header}/** Plain text for /integrate “What you proved” — source: docs/epistemic-contract.md integrator fence. */
export const EPISTEMIC_CONTRACT_INTEGRATOR_SNIPPET: string = ${escaped};
`;

writeFileSync(join(outDir, "epistemicContractIntegrator.ts"), body, "utf8");
console.log("Wrote website/src/generated/epistemicContractIntegrator.ts");
