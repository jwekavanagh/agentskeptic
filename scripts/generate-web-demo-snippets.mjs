#!/usr/bin/env node
/**
 * Regenerates website/src/content/demoExampleSnippets.ts from the live engine.
 * Run from repo root after `npm run build`:
 *   node scripts/generate-web-demo-snippets.mjs
 */
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const examplesDir = path.join(root, "examples");
const outPath = path.join(root, "website", "src", "content", "demoExampleSnippets.ts");

const distIndex = pathToFileURL(path.join(root, "dist", "index.js")).href;
const { verifyWorkflow } = await import(distIndex);

const scenarios = ["wf_complete", "wf_missing", "wf_inconsistent"];

const snippets = {};
for (const scenarioId of scenarios) {
  let truthReportText = "";
  const workflowResult = await verifyWorkflow({
    workflowId: scenarioId,
    eventsPath: path.join(examplesDir, "events.ndjson"),
    registryPath: path.join(examplesDir, "tools.json"),
    database: { kind: "sqlite", path: path.join(examplesDir, "demo.db") },
    logStep: () => {},
    truthReport: (s) => {
      truthReportText = s;
    },
  });
  const workflowResultJson = JSON.stringify(workflowResult, null, 2);
  snippets[scenarioId] = { workflowResultJson, truthReportText };
}

function esc(s) {
  return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

let ts = `/**\n * Auto-generated by scripts/generate-web-demo-snippets.mjs — do not edit by hand.\n */\n\n`;
ts += `export const demoExampleSnippets = {\n`;
for (const scenarioId of scenarios) {
  const sn = snippets[scenarioId];
  ts += `  ${scenarioId}: {\n`;
  ts += `    workflowResultJson: \`${esc(sn.workflowResultJson)}\`,\n`;
  ts += `    truthReportText: \`${esc(sn.truthReportText)}\`,\n`;
  ts += `  },\n`;
}
ts += `} as const;\n`;

mkdirSync(path.dirname(outPath), { recursive: true });
writeFileSync(outPath, ts, "utf8");
console.log("wrote", outPath);
